public class Variables {
    public static void main(String[] args) {
        // Variable naming rules
        // 1. Variable name should not start with a digit
        // 2. Variable name should not contain any special character except underscore(_)
        // 3. Variable name should not be a reserved keyword
        // 4. Variable name should not contain any space
        // 5. Variable names cannot be data type names
        int x = 10;
        int y;
        y = 20;
        System.out.println(x+y);
        System.out.println(x*y);
        int z = 50;
        System.out.println(z);
        x = x * 10;
        System.out.println(x);
        x = x/2;
        System.out.println(x);

    }
}
