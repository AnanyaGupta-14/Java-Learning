public class DivisionOfDataTypes {
    public static void main(String[] args) {
        double x = 5/2;
        System.out.println(x);

        double y = 5.0/2;
        System.out.println(y);

        double z = 5.0/2.0;
        System.out.println(z);

        double w = 5/2.0;
        System.out.println(w);
    }
}
