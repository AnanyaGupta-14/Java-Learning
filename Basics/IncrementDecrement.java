public class IncrementDecrement {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(x);
        x ++;  // x =  x +1 Or x += 1
        System.out.println(x);
        x--;
        System.out.println(x);

        int y  = 12;
        System.out.println(y++);
        System.out.println(y);

        int z = 15;
        System.out.println(++z);
        System.out.println(z);

        int a = 20;
        int b = a++;
        System.out.println(a + " " + b);

}
}
