public class RelationalOperators {
    public static void main(String[] args) {
        System.out.println(10 > 5); // true
        System.out.println(10 < 5); // false
        System.out.println(10 >= 5); // true
        System.out.println(10 <= 5); // false
        System.out.println(10 == 5); // false
        System.out.println(10 != 5); // true

        //Using Boolean Data type
        boolean a = (10 > 5);
        System.out.println(a);
        
    }
}
