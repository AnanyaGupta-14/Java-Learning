public class ArithematicOperation {
    public static void main(String[] args) {
        int x = 5, y= 10;
        System.out.println("Addition of x and y is: " + (x+y));
        System.out.println("Subtraction of x and y is: " + (x-y));
        System.out.println("Multiplication of x and y is: " + (x*y));
        System.out.println("Division of x and y is: " + (x/y));
        System.out.println("\n");
        double z= 5, w= 10;
        System.out.println("Addition of z and w is: " + (z+w));
        System.out.println("Subtraction of z and w is: " + (z-w));
        System.out.println("Multiplication of z and w is: " + (z*w));
        System.out.println("Division of z and w is: " + (z/w));

        
        
}
}
